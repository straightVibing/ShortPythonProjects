#addition

#program to calculate the addition of 2 numbers


#Inputs
firstNumber = input('First number?:')   #allows the user to input their first number
secondNumber = input('Second number?:') #allows the user to enter their second number

#Conversions
firstNumber = float(firstNumber)    #Converts firstNumber from a string to a floating point number
secondNumber = float(secondNumber)  #Converts secondNumber from a string to a floating point number

#Calculation
result = firstNumber + secondNumber #calculates the total of firstNumber plus secondNumber

#Display
print(result)   #prints the result of the calculation onto the shell
